package uytube.logica;

public class DtFecha {
	private int dia;
	private int mes;
	private int anio;	
	public DtFecha(int d,int m,int a) {
		dia = d;
		mes = m;
		anio = a;
	}
	
	public int getDia() {
		return dia;
	}
	
	public int getMes() {
		return mes;
	}
	
	public int getAnio() {
		return anio;
	}
		
}
